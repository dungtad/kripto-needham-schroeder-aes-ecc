        // TODO add your handling code here:
        String user = jTextField1.getText();
        String pass1 = jPasswordField1.getText();
        String pass2 = jPasswordField2.getText();
        if(user.isEmpty() || !pass1.equals(pass2) || pass1.isEmpty() || pass2.isEmpty())
            JOptionPane.showMessageDialog(this, "Las claves no coinciden!", "Error", JOptionPane.ERROR_MESSAGE);
        else {
             this.cPrincipal.agregarUsuario(user,pass1);
             JOptionPane.showMessageDialog(this, "Usuario agregado con éxito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
             this.cPrincipal.cargarUsuarios();
        }
