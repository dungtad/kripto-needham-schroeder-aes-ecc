    public void cargarUsers(Vector lista) {



        DefaultTableModel modelo ;
        for(int i=0;i<lista.size();i++)
        {
            if(jTable1.getRowCount()<lista.size()){
                   //vamos agregando mas columnas
            modelo = (DefaultTableModel)jTable1.getModel();
            modelo.addRow(new Object[1]);
            }
            jTable1.setValueAt(((Usuario)lista.get(i)).getUser(), i,0 );
            jTable1.setValueAt(((Usuario)lista.get(i)).getPass(), i,1 );

         
        }

    }

